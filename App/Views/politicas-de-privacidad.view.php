  <h2>3. Glosario</h2>
  <p>De acuerdo con la ley, se entienden los siguientes conceptos:</p>
  <ul>
    <li><strong>Titular:</strong> Persona natural cuyos datos son objeto de tratamiento.</li>
    <li><strong>Tratamiento:</strong> Recolección, almacenamiento, uso, circulación o supresión.</li>
    <li><strong>Responsable:</strong> Persona natural o jurídica que decide sobre la base de datos.</li>
    <li><strong>Encargado:</strong> Persona natural o jurídica que realiza el tratamiento por encargo del responsable.</li>
    <li><strong>Dato público:</strong> Información no clasificada como privada o sensible.</li>
    <li><strong>Dato privado:</strong> Información de carácter personal e íntimo.</li>
    <li><strong>Dato sensible:</strong> Información que afecta la intimidad del titular o genera riesgo de discriminación.</li>
    <li><strong>Transmisión:</strong> Tratamiento de datos por un encargado dentro o fuera del país.</li>
    <li><strong>Transferencia:</strong> Envío de datos a otro responsable dentro o fuera del país.</li>
  </ul>

  <h2>4. Derechos de los Titulares</h2>
  <p>Los titulares pueden ejercer los siguientes derechos:</p>
  <ul>
