    <li>Solicitar prueba de la autorización otorgada.</li>
    <li>Conocer el uso dado a su información.</li>
    <li>Presentar quejas ante la Superintendencia de Industria y Comercio (SIC).</li>
    <li>Revocar la autorización cuando no se respeten principios legales.</li>
    <li>Conocer las personas responsables de atender consultas o reclamos.</li>
  </ul>

  <h2>5. Tratamiento de Datos de Menores</h2>
  <p>
